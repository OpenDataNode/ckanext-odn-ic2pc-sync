$( document ).ready(function() {
	$('form').bind('submit', function() {
        $('#authorization').removeAttr('disabled');
    });
	
	$( "#authorization_required" ).change(function () {
	    checked = $( "#authorization_required" ).prop('checked');
	    if(checked) {
	    	$("#authorization").removeAttr('disabled');
	    } else {
	    	$("#authorization").attr('disabled','disabled');
	    }
	  });
	
	$( "#authorization_required" ).change();
	
	$('input[type="text"]').on('input', function() {
		formChanged();
	});
	
	$('input:not([type="text"])').on('change', function() {
		formChanged();
	});
	
	$('#url').on('input', function () {
		checkUrl($('#url').val());
	});
});

function formChanged() {
	$('input[name="verify"]').attr('type', 'submit');
	$('input[name="save"]').attr('type', 'hidden');
	$('input[name="action"]').val('verify');
}

function checkUrl(val) {
	isValid = isUrlValid(val);
	if(isValid) {
		$('input[type="submit"]').removeAttr('disabled');
	} else {
		$('input[type="submit"]').attr('disabled','disabled');
	}
}

